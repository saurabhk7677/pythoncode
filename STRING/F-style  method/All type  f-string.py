# integer f-string format.....

a = 10
b = 20
print(f"{a}")
print(f"{a} {b}")
print(f"{b} {a}")
print()

value = f"{a}"
print(value)

##################################################################################################

# Float f-string format......

c = 10.50
d = 20.42
print(f"{c}")
print(f"{c} {d}")
print(f"{d} {c}")
print()

######################################################################################################

# String F-string format......

f_name = "Geeky"
l_name = "Shows"
print(f"{f_name}")
print(f"{f_name} {l_name}")
print(f"{l_name} {f_name}")
print()

########################################################################################################

# Integer and String Combine format......

name = "GeekyShows"
age = 10
print(f"Hello my name is {name}")
print(f"{name} {age}")
print(f"{age} {name}")

