# APPEND METHOD()......
a = [10, 20, -50, 21.3, 'Geekyshows']
a.append(100)
print(a)
print()


# INSERT METHOD()......
b = [23, 35, -324, 42.34, 'saurabh']
b.insert(2,'kadam')
print(b)
print()


# POP()....
c = [23, 35, -324, 42.34, 'saurabh']
c.pop()
print(c)
print()


# POP(N)...............
d = [23, 35, -324, 42.34, 'saurabh']
d.pop(3)
print(d)
print()


# REMOVE().....
e = [23, 35, -324, 42.34, 'saurabh']
e.remove(-324)
print(e)
print()


# INDEX()......
f = [23, 35, -324, 42.34, 'saurabh']
num = f.index(42.34)
print(num)
print()


# REVERSE().......
g = [23, 35, -324, 42.34, 'saurabh']
g.reverse()
print(g)
print()


# EXTEND().......
h = [23, 35, -324, 42.34, 'saurabh']
i = [43,45,98,'fdhggf']
h.extend(i)
print(h)
print()


# COUNT().......
j = [23, 35, -324, 42.34, 35, 35, 43,'saurabh']
sk = j.count(35)
print(sk)
print()  



# SORT()......
b = [23, 35, -324, 42.34, 43, 67,12]
b.sort()
print(b)
print()



# CLEAR().........
z = [23, 35, -324, 42.34, 'saurabh']
